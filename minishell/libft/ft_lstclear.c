#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;
	t_list	*list;

	if (lst && del)
	{
		list = *lst;
		temp = NULL;
		while (list)
		{
			temp = list;
			list = list->next;
			del(temp->content);
			free(temp);
			temp = NULL;
		}
		free(*lst);
		*lst = NULL;
	}
}
