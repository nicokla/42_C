#include "libft.h"

void	*ft_calloc(size_t nmenb, size_t size)
{
	void	*arr;

	if (nmenb == 0 || size == 0)
	{
		nmenb = 1;
		size = 1;
	}
	arr = malloc(nmenb * size);
	if (arr == 0)
		return (0);
	ft_bzero(arr, nmenb * size);
	return (arr);
}
