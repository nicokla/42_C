#include "libft.h"
#include <stdlib.h>
#include "minishell.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	if (!(str = (char*)malloc(sizeof(*str) * (size + 1))))
		fatal_error(strerror(errno));
	ft_bzero(str, size + 1);
	return (str);
}
